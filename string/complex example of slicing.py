#string slicing with step size 
s="IIT BOMBAY"
print(s[0:len(s):2])

#to print the entire string 
print(s[::])

#to display the string in reverse order
print(s[::-1])

#to access the characters of a string from index -1
print(s[-1:0:-1])

#to start with the character stored at index 0 and exclude the last character stored at index -1
print(s[:-1])
